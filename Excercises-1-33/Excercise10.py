from math import log10
a = int(input("Enter the first integer: "))
b = int(input("Enter the second integer: "))

print(a,"+", b, "is", a+b)
print(a,"-", b, "is", a-b)
print(a,"*", b, "is", a*b)
print(a,"/", b, "is", a/b)
print(a,"%", b, "is", a%b)
print("The base 10 logarithm of ", a,"is", log10(a))
print(a, "^", b, "is", a**b)



